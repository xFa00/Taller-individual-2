    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double A,B,C;
        
        A= Double.parseDouble(txtValorA.getText());
        B= Double.parseDouble(txtValorB.getText());
        C= Double.parseDouble(txtValorC.getText());
        
        Double[] soluciones=Funcion.solucion(A, B, C);
        
        if (soluciones == null) {
        // No hay soluci贸n real
        txtSolucion1.setText("La ecuaci贸n no tiene soluci贸n real.");
    } else if (soluciones.length == 1) {
        // Una soluci贸n real doble
        double solucion = soluciones[0];
        txtSolucion1.setText("La solucion es: "+solucion);
    } else {
        // Dos soluciones reales distintas
        double solucionUno = soluciones[0];
        double solucionDos = soluciones[1];
        txtSolucion1.setText("La solucion numero 1 es: " + solucionUno);
        txtSolucion2.setText("La solucion numero 2 es: "+ solucionDos);
    }
              
    }//GEN-LAST:event_btnCalcularActionPerformed

